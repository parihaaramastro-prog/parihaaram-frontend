"use client";

import { createClient } from "@/lib/supabase";
import { UserRole } from "@/lib/services/profile";
import { motion } from "framer-motion";
import { Shield, ArrowLeft } from "lucide-react";

import Link from "next/link";

interface LoginPageProps {
    role: UserRole;
    title: string;
    description: string;
    redirectPath: string;
}

export default function GenericLoginPage({ title, description, redirectPath }: LoginPageProps) {
    const supabase = createClient();

    const handleGoogleLogin = async () => {
        await supabase.auth.signInWithOAuth({
            provider: 'google',
            options: {
                redirectTo: `${window.location.origin}/auth/callback?next=${redirectPath}`,
            },
        });
    };

    return (
        <main className="min-h-screen flex items-center justify-center px-6">
            <div className="w-full max-w-md space-y-8">
                <div className="text-center space-y-4">
                    <motion.div
                        initial={{ scale: 0.9, opacity: 0 }}
                        animate={{ scale: 1, opacity: 1 }}
                        className="inline-flex items-center justify-center w-16 h-16 rounded-2xl bg-indigo-50 border border-indigo-100 text-indigo-600 mb-4"
                    >
                        <Shield className="w-8 h-8" />
                    </motion.div>
                    <h1 className="text-3xl font-bold text-slate-900 tracking-tight uppercase">{title}</h1>
                    <p className="text-slate-500 text-sm font-medium">{description}</p>
                </div>

                <div className="bg-white p-8 rounded-2xl border border-slate-200 shadow-xl space-y-6">
                    <button
                        onClick={handleGoogleLogin}
                        className="w-full h-12 bg-white border border-slate-200 hover:bg-slate-50 hover:border-slate-300 text-slate-700 rounded-xl font-bold text-sm tracking-wide transition-all shadow-sm flex items-center justify-center gap-3 active:scale-95"
                    >
                        <svg className="w-5 h-5" viewBox="0 0 24 24">
                            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                        </svg>
                        <span>Continue with Google</span>
                    </button>

                    <div className="pt-4 text-center">
                        <Link href="/" className="inline-flex items-center gap-2 text-[10px] font-bold text-slate-400 hover:text-indigo-600 transition-colors uppercase tracking-widest">
                            <ArrowLeft className="w-3 h-3" />
                            Return to Public Surface
                        </Link>
                    </div>
                </div>

                <div className="text-center opacity-30">
                    <p className="text-[9px] font-bold text-slate-500 uppercase tracking-[0.3em]">SECURE ACCESS PROTOCOL 4.2.0</p>
                </div>
            </div>
        </main>
    );
}
